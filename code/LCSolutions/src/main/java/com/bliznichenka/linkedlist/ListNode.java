package com.bliznichenka.linkedlist;

/**
 * A class which represents a node of a linked list as per Leetcode definition
 */
public class ListNode {
    public int val;
    public ListNode next;

    public ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
